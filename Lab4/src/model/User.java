package model;

/**
 * User class representing user entity in database
 */
public class User {
    private int id;        // Assuming there is an 'id' field in your database table
    private String name;   // Corresponds to the 'NAME' column in database
    private int age;       // Corresponds to the 'AGE' column in database
    private  String FirstName;
    private  String LastName;

    // Constructors
    public User() {
    }

    public User(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public User(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    // toString method for debugging
    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
